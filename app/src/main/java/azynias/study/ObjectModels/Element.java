public class Element {
    private String element;
    private int ID;

    public Element(String element) {
        this.element = element;

    }

    public String getElement() {
        return element;
    }

    public void setElement(String element) {
        this.element = element;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }




}
